package com.pb.client;

public class DniDto {

	private int no;
	private String name;
	private double interest_rate;
	private String type;
	private String calc_type;
	private String gender_restriction;
	private String job_restriction;
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getInterest_rate() {
		return interest_rate;
	}
	public void setInterest_rate(double interest_rate) {
		this.interest_rate = interest_rate;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getCalc_type() {
		return calc_type;
	}
	public void setCalc_type(String calc_type) {
		this.calc_type = calc_type;
	}
	public String getGender_restriction() {
		return gender_restriction;
	}
	public void setGender_restriction(String gender_restriction) {
		this.gender_restriction = gender_restriction;
	}
	public String getJob_restriction() {
		return job_restriction;
	}
	public void setJob_restriction(String job_restriction) {
		this.job_restriction = job_restriction;
	}
	
		
}

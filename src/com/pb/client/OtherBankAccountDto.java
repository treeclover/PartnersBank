package com.pb.client;

public class OtherBankAccountDto {
	private String bank_name;
	private String other_bank_client;
	private String account_number;
	private String account_name;
	private String trade_content;
	private String note;
	private String trade_time;
	public String getBank_name() {
		return bank_name;
	}
	public void setBank_name(String bank_name) {
		this.bank_name = bank_name;
	}
	public String getOther_bank_client() {
		return other_bank_client;
	}
	public void setOther_bank_client(String other_bank_client) {
		this.other_bank_client = other_bank_client;
	}
	public String getAccount_number() {
		return account_number;
	}
	public void setAccount_number(String account_number) {
		this.account_number = account_number;
	}
	public String getAccount_name() {
		return account_name;
	}
	public void setAccount_name(String account_name) {
		this.account_name = account_name;
	}
	public String getTrade_content() {
		return trade_content;
	}
	public void setTrade_content(String trade_content) {
		this.trade_content = trade_content;
	}
	public String getNote() {
		return note;
	}
	public void setNote(String note) {
		this.note = note;
	}
	public String getTrade_time() {
		return trade_time;
	}
	public void setTrade_time(String trade_time) {
		this.trade_time = trade_time;
	}
	
}
